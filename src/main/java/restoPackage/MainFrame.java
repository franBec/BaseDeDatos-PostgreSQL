    private void jButton_Mesa_byMozoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_Mesa_byMozoActionPerformed
        try {
            int input = Integer.parseInt(JOptionPane.showInputDialog(null, "Ingrese CODIGO del Mozo", "Mostrar Mesas atendidas por un Mozo", JOptionPane.QUESTION_MESSAGE));
            jTable_TABLA_Mesa_byMozo.setModel(resultToTable(Queries.mesasAtendidas_byMozoCOD(input)));
            if(jTable_TABLA_Mesa_byMozo.getRowCount()==0)
                JOptionPane.showMessageDialog(null, "El mozo solicitado\n✓No tiene ninguna mesa asignada\nó\n✘No existe", "Mozo sin Mesas asignadas", JOptionPane.INFORMATION_MESSAGE);
            else{
                jLabel_MoCodAtiende.setText("El Mozo con CODIGO = "+String.valueOf(input));
                
                jTable_TABLA_Mesa_byMozo.getColumnModel().getColumn(0).setHeaderValue("Código de la mesa"); jTable_TABLA_Mesa_byMozo.getTableHeader().resizeAndRepaint();
                jTable_TABLA_Mesa_byMozo.getColumnModel().getColumn(1).setHeaderValue("Sector"); jTable_TABLA_Mesa_byMozo.getTableHeader().resizeAndRepaint();
                
                Panel_Mesa_byMozo.setVisible(true);
                Panel_CONSULTAS.setVisible(false);
            }
        }
        catch (SQLException ex) {
            catchSQLException(ex);
        } 
        catch (java.lang.NumberFormatException e){
            catchOperacionCancelada();
        }
        catch(UnsupportedEncodingException e){
            catchUnsupportedEncodingException();
        }
    }//GEN-LAST:event_jButton_Mesa_byMozoActionPerformed

